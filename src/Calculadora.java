public class Calculadora {

    public double suma(double n1, double n2) {
        return n1 + n2;
    }

    public double resta(double n1, double n2) {
        return n1 - n2;
    }

    public double multiplica(double n1, double n2) {
        return n1 * n2;
    }

    public double divide(double n1, double n2) {
        return n1 / n2;
    }
}
